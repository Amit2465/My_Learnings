package Basic_java;

public class Operators {
   public static void main(String[] args) {
    // Arithmetic operator
    int a=10;
    int b=12;
    System.out.println(a+b);
    System.out.println(a-b);
    System.out.println(a*b);
    System.out.println(a/b);
    System.out.println(a%b);
    System.out.println(a^b);
    System.out.println(a++);
    System.out.println(a--);

    //Relational operations
    System.out.println(a==b);
    System.out.println(a!=b);
    System.out.println(a>b);
    System.out.println(a<b);
    System.out.println(a>=b);
    System.out.println(a<=b);

        
   }
}
